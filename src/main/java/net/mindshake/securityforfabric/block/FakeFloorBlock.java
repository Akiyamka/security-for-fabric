public class FakeFloorBlock extends Block{

    public FakeFloorBlock(Settings settings) {
        super(settings);
    }

    @Override
    public void onSteppedOn(World world, BlockPos pos, BlockState state, Entity entity) {
        if (!world.isClient) {
            ((ServerWorld)world).breakBlock(pos, false);
        }
    }
}
